# Wrapper aliases for make commands (run `make help` for full list)
alias vc="make -C ${PATH_TO_VOLUME_CONTROL_REPO}"
alias vc-help="vc readme"
alias vc-start="vc start-bg"
alias vc-run="vc run"
alias vc-logs="vc logs"
alias vc-list="vc list"
alias vc-kill="vc kill"
alias vc-restart="vc kill && vc start-bg"
